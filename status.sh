#!/bin/bash
# status.sh - Проверка статуса

echo "=========================================="
echo "📊 СТАТУС ПРОЕКТА"
echo "=========================================="

# Проверка порта 5000
if lsof -i:5000 > /dev/null 2>&1; then
    echo "🟢 Удалённый решатель: ЗАПУЩЕН (порт 5000)"
else
    echo "🔴 Удалённый решатель: НЕ ЗАПУЩЕН"
fi

# Проверка порта 8501
if lsof -i:8501 > /dev/null 2>&1; then
    echo "🟢 Streamlit интерфейс: ЗАПУЩЕН (порт 8501)"
else
    echo "🔴 Streamlit интерфейс: НЕ ЗАПУЩЕН"
fi

echo "=========================================="